        // 　　■
        // ■■■
        [ 0b00000000001000000000; 0b00000000111000000000; ]
        [ 0b00000000010000000000; 0b00000000010000000000; 0b00000000011000000000; ]
        [ 0b00000000111000000000; 0b00000000100000000000; ]
