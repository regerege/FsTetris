namespace FsTetris

module TetrisCommon =
    let _random = new System.Security.Cryptography.RNGCryptoServiceProvider()
    let inline rand max =
        let mutable b = [| 0uy |]
        _random.GetBytes(b)
        let r = new System.Random(int(b.[0]))
        r.Next(0, max)

